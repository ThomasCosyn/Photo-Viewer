def month_decoder(month_number):
    months = {'01': 'Janvier', '02': 'Février', '03': 'Mars', '04': 'Avril',
              '05': 'Mais', '06': 'Juin', '07': 'Juillet', '08': 'Août',
              '09': 'Septembre', '10': 'Octobre', '11': 'Novembre',
              '12': 'Décembre'}
    return months[month_number]
